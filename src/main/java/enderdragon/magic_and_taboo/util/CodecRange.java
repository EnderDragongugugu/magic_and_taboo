package enderdragon.magic_and_taboo.util;

public class CodecRange {
    public float min;
    public float max;

    public CodecRange(float min, float max) {
        this.min = min;
        this.max = max;
    }

    public float getMin() {
        return min;
    }

    public float getMax() {
        return max;
    }
}
